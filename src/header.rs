pub fn entry_point(rom: &[u8]) -> &[u8] {
    &rom[0x100..0x104]
}

pub fn logo(rom: &[u8]) -> &[u8] {
    &rom[0x104..0x0134]
}

pub fn title(rom: &[u8]) -> &[u8] {
    &rom[0x134..0x144]
}

pub fn manufacturer(rom: &[u8]) -> &[u8] {
    &rom[0x013F..0x143]
}

pub fn version(rom: &[u8]) -> u8 {
    rom[0x14C]
}
